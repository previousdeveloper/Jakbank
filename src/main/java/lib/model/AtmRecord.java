package lib.model;


public class AtmRecord {

    private String city;
    private String district;
    private String address;
    private String name;
    private String latitude;
    private String longitude;
    private String deviceType;
    private boolean exchangeAvailable;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(String deviceType) {
        this.deviceType = deviceType;
    }

    public boolean isExchangeAvailable() {
        return exchangeAvailable;
    }

    public void setExchangeAvailable(boolean exchangeAvailable) {
        this.exchangeAvailable = exchangeAvailable;
    }
}
